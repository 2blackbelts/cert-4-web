<h2>Origins of the Exclamation Mark</h2>
<p>The origin of the exclamation mark is uncertain!!!!<br /><br /><br /></p> 
<p>The first exclamation mark appeared in print around 1400.</p> 
<p>It is thought to derives from the Latin <strong>Io </strong> which means joy!!!! </p>
<p>
  Perhaps, one day lost in the mists of time, somebody wrote a joyful upbeat sentence and to 
  emphasise that, they finished the sentence by putting the second letter of Io under the first!!!!!
</p>
